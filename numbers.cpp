#include <iostream>

using namespace std;

int main() {
  // addition
  cout << 40+5 << endl;
  //modulo
  cout << 10 % 3 << endl;
  int wnum = 5;
  double dnum = 5.5;
  wnum++;
  cout << ++wnum << endl;
  // double and int returns double
  cout << dnum + 5 << endl;
  // int divide by int returns a integer
  cout << 10 / 3 << endl;
  
  return 0;
}