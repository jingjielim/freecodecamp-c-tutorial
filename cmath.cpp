#include <iostream>
// Standard math library
#include <cmath>

using namespace std;

int main()
{
  //Power
  cout << pow(2, 5) << endl;
  cout << sqrt(36) << endl;
  cout << round(6.4) << endl;
  cout << floor(9.6) << endl;
  cout << ceil(10.1) << endl;
  // max of two numbers
  cout << fmax(6, 7) << endl;
  // min of two numbers
  cout << fmin(6, 7) << endl;
  
  return 0;
}